// bucle

//while
console.log("\n[While]")
let i = 1
while(i<4)
{
    console.log(`iteration: ${i}`);
    i++
}


// do while (first iteration is always executed)
console.log("\n[Do while]")
let j = 1
do
{
    console.log(`iteration: ${j}`);
    j++
} while(j<4)

//for 
console.log("\n[For]")
for (k = 1; k < 4; k++)
{
    console.log(`iteration: ${k}`);
}
